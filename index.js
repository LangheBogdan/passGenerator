let passOneEl = document.getElementById("pass-one-el"); //?
let passTwoEl = document.getElementById("pass-two-el"); //?
let generatePasswordEl = document.getElementById("generate-password-el"); //?
function generatePassword(length) {
  let password = "";
  for (let i = 0; i < length; i++) {
    const randomIndex = Math.floor(Math.random() * characters.length);
    password += characters[randomIndex];
  }
  return password;
}

generatePasswordEl.addEventListener("click", function () {
  const length = document.getElementById("passwordLength").value || 15;
  const password1 = generatePassword(length);
  const password2 = generatePassword(length);
  passOneEl.textContent = password1;
  passTwoEl.textContent = password2;
});

